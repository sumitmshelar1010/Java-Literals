package MainPackage.JavaBasics.literals;

public class BackslashLiterals {


    public static void main(String[] args) {
        //-------------- Backslash Literals----------//

        System.out.println("Name \n sumit");   //  \n for new Line
        System.out.println("Name \t sumit");   //  \t for horizontal tab
        System.out.println("\'Sumit");         //  \' for single quote
        System.out.println("\"Sumit")  ;       //  \" for Double quote
    }
}
