package MainPackage.JavaBasics.literals;

public class BooleanLiteral {
    public static void main(String[] args) {
        //-------------------  Boolean Literals--------------------

        boolean b=true;
        boolean c=false; // it is always between true or false
        System.out.println(b);
        System.out.println(c);
    }

    public static class Introduction {//Literals
        //Integer Literals
        public static void main(String[] args){
            // FOR DECIMAL FORM
            int A = 101;
            // FOR OCTAL FORM
            int B = 0101;
            // FOR HEXA DECIMAL FORM
            int C = 0x101af;
            // FOR BINARY FORM
            int D = 0b101;

            System.out.println(A);
            System.out.println(B);
            System.out.println(C);
            System.out.println(D);
        }
    }
}
