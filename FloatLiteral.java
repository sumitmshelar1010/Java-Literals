package MainPackage.JavaBasics.literals;

public class FloatLiteral {
    public static void main(String[] args) {


        //Float and Double
        float d = 10.1f;         //(Suffix f)
        float e = 1010.1F;       //(Suffix F)

 // If we have not mensioned any suffix so it will be default consider as double type

        double g= 102.0d;       //(Suffix d)
        double h = 10.5D;        //(Suffix D)
        System.out.println(d);
        System.out.println(e);
        System.out.println(d);
        System.out.println(h);
    }
}
